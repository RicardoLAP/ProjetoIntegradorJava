
package Funcionario;


public class Funcionario {
    
    private String Nome;
    private String Cpf;
    private String Enedere;
    private String Telefo;
    private String Email;
    private String DataAdm;
    private String Cargo;
    private String Salario;
    private String Departa;

    public String getNome() {
        return Nome;
    }

    public void setNome(String Nome) {
        this.Nome = Nome;
    }

    public String getCpf() {
        return Cpf;
    }

    public void setCpf(String Cpf) {
        this.Cpf = Cpf;
    }

    public String getEnedere() {
        return Enedere;
    }

    public void setEnedere(String Enedere) {
        this.Enedere = Enedere;
    }

    public String getTelefo() {
        return Telefo;
    }

    public void setTelefo(String Telefo) {
        this.Telefo = Telefo;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String Email) {
        this.Email = Email;
    }

    public String getDataAdm() {
        return DataAdm;
    }

    public void setDataAdm(String DataAdm) {
        this.DataAdm = DataAdm;
    }

    public String getCargo() {
        return Cargo;
    }

    public void setCargo(String Cargo) {
        this.Cargo = Cargo;
    }

    public String getSalario() {
        return Salario;
    }

    public void setSalario(String Salario) {
        this.Salario = Salario;
    }

    public String getDeparta() {
        return Departa;
    }

    public void setDeparta(String Departa) {
        this.Departa = Departa;
    }
    
}
