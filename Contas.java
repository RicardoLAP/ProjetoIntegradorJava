
package Contas;


public class Contas {
    
    private String Desconta;
    private String Valor;
    private String DataVe;
    private String DataPa;
    private String Fornec;
    private String Cate;
    private String Status;
    private String Metodo;
    private String Obs;   

    public String getDesconta() {
        return Desconta;
    }

    public void setDesconta(String Desconta) {
        this.Desconta = Desconta;
    }

    public String getValor() {
        return Valor;
    }

    public void setValor(String Valor) {
        this.Valor = Valor;
    }

    public String getDataVe() {
        return DataVe;
    }

    public void setDataVe(String DataVe) {
        this.DataVe = DataVe;
    }

    public String getDataPa() {
        return DataPa;
    }

    public void setDataPa(String DataPa) {
        this.DataPa = DataPa;
    }

    public String getFornec() {
        return Fornec;
    }

    public void setFornec(String Fornec) {
        this.Fornec = Fornec;
    }

    public String getCate() {
        return Cate;
    }

    public void setCate(String Cate) {
        this.Cate = Cate;
    }

    public String getStatus() {
        return Status;
    }

    public void setStatus(String Status) {
        this.Status = Status;
    }

    public String getMetodo() {
        return Metodo;
    }

    public void setMetodo(String Metodo) {
        this.Metodo = Metodo;
    }

    public String getObs() {
        return Obs;
    }

    public void setObs(String Obs) {
        this.Obs = Obs;
    }
    
}
