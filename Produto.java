
package Produto;


public class Produto {
    
    private String NomeProd;
    private String CodProd;
    private String Descr;
    private String Preco;
    private String QuantEsto;
    private String Catego;
    private String DataVa;
    private String Fornce;
    private String LocalEsto;

    public String getNomeProd() {
        return NomeProd;
    }

    public void setNomeProd(String NomeProd) {
        this.NomeProd = NomeProd;
    }

    public String getCodProd() {
        return CodProd;
    }

    public void setCodProd(String CodProd) {
        this.CodProd = CodProd;
    }

    public String getDescr() {
        return Descr;
    }

    public void setDescr(String Descr) {
        this.Descr = Descr;
    }

    public String getPreco() {
        return Preco;
    }

    public void setPreco(String Preco) {
        this.Preco = Preco;
    }

    public String getQuantEsto() {
        return QuantEsto;
    }

    public void setQuantEsto(String QuantEsto) {
        this.QuantEsto = QuantEsto;
    }

    public String getCatego() {
        return Catego;
    }

    public void setCatego(String Catego) {
        this.Catego = Catego;
    }

    public String getDataVa() {
        return DataVa;
    }

    public void setDataVa(String DataVa) {
        this.DataVa = DataVa;
    }

    public String getFornce() {
        return Fornce;
    }

    public void setFornce(String Fornce) {
        this.Fornce = Fornce;
    }

    public String getLocalEsto() {
        return LocalEsto;
    }

    public void setLocalEsto(String LocalEsto) {
        this.LocalEsto = LocalEsto;
    }
          
    
}
